#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*res;

	len = ft_strlen(s1);
	res = (char *)malloc(++len);
	if (!res)
		return (NULL);
	(void)ft_memcpy(res, s1, len);
	return (res);
}
